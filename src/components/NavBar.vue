<script setup>
import { onMounted } from 'vue'
import { useRouter } from 'vue-router'
import { useAuth } from '@/composables/useAuth'

const router = useRouter()
const { user, logout } = useAuth()

async function handleLogout() {
  try {
    console.log('🚪 Intentando cerrar sesión...')
    await logout()
    console.log('✅ Sesión cerrada exitosamente')
    router.push('/login')
  } catch (error) {
    console.error('❌ Error al cerrar sesión:', error)
  }
}

// Debug: verificar que el usuario está presente
onMounted(() => {
  console.log('🔍 NavBar montado - Usuario:', user)
})
</script>

<template>
  <nav class="fixed top-0 left-0 right-0 w-full bg-blue-600 text-white shadow-md z-50">
    <div class="container mx-auto flex items-center justify-between py-3 px-4">
      <!-- Logo y enlaces -->
      <div class="flex items-center space-x-6">
        <a @click="router.push('/home')" class="text-xl font-bold cursor-pointer hover:text-blue-200">
          AdWeb Online
        </a>
        <a @click="router.push('/home')" class="cursor-pointer hover:text-blue-200">
          Cursos
        </a>
        <a @click="router.push('/admin')" class="cursor-pointer hover:text-blue-200">
          Administración
        </a>
      </div>
      
      <!-- SIEMPRE mostrar botón de cerrar sesión -->
      <div class="flex items-center gap-3">
        <!-- Mostrar email si hay usuario -->
        <span v-if="user" class="text-sm font-medium bg-blue-700 px-3 py-1 rounded">{{ user.email }}</span>
        
        <!-- Botón de cerrar sesión - SIEMPRE VISIBLE -->
        <button 
          @click="handleLogout" 
          type="button"
          class="bg-red-600 hover:bg-red-700 active:bg-red-800 text-white font-bold px-6 py-3 rounded-lg shadow-lg hover:shadow-xl transition-all duration-200 border-2 border-red-800"
          style="min-width: 140px; font-size: 16px; z-index: 9999; position: relative;"
        >
          🚪 SALIR
        </button>
      </div>
    </div>
  </nav>
</template>

